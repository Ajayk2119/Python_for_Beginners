# Variable is the memory location to store the value, in below examples we will store different type data type into variables
a = 10   # int type
b = 12.10  # float type
c = "My data" # string type
d = True # boolean type
e = [1, 2, 3, 4] #list type
f = (1, 2, 3) #tuple type
g = {"a": 2}  # dict type

print(type(a), type(b), type(c), type(d), type(e), type(f), type(g))

print(a, b, c, d, e, f, g)
